/**
 * Get icons by category
 */
export function getIconsByCategory(category: string): IconSet {
  switch (category) {
    case 'ui': return UI_ICONS;
    case 'arrow': return ARROW_ICONS;
    case 'contact': return CONTACT_ICONS;
    case 'social': return SOCIAL_ICONS;
    case 'business': return BUSINESS_ICONS;
    case 'industry': return INDUSTRY_ICONS;
    default: return {};
  }
}

/**
 * Get a single icon by name
 */
export function getIcon(name: string): string | null {
  const allIcons = getAllIcons();
  return allIcons[name] || null;
}

